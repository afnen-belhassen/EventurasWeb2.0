public function searchByName(string $query): array
{
    return $this->createQueryBuilder('e')
        ->where('e.title LIKE :query')
        ->setParameter('query', '%'.$query.'%')
        ->orderBy('e.date_event', 'ASC')
        ->getQuery()
        ->getResult();
}
